#메뉴목록

def 커피주문(주문):
    메뉴목록=[{'메뉴명':'라떼','가격':4},
             {'메뉴명':'아메리카노','가격':3}]
    
    for 메뉴 in 메뉴목록:
        if 주문 == 메뉴['메뉴명']:
            # return True #메뉴 있음
            return 메뉴['가격'] # 있으면 가격 반환

    #다 찾아봤는데 없음.
    # return False
    return -1

#print(커피주문('라떼')) #True
print(커피주문('라떼')) # 4
print(커피주문('녹차')) # -1
