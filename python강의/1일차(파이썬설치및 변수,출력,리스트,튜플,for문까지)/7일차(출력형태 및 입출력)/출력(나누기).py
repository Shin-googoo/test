print(4/3)
print('{0}'.format(4/3))  # print(4/3)과 동일
print('{0:e}'.format(4/3)) #지수표시
print('{0:f}'.format(4/3)) #소수점으로 표시
print('{0:%}'.format(4/3)) #%로 표시하라

print('소수점 자리수 표현')
print('{0:.1f}'.format(4/3)) #소수점 1자리까지만 표현해라
print('{0:.2f}'.format(4/3)) #소수점 2자리까지만 표현해라
print('{0:.3f}'.format(4/3)) #소수점 3자리까지만 표현해라

